int GetChoseongSet(int Jungseong, int Jongseong)
{
    if (!Jongseong) {
        static int Set[] = {
            0, 0, 0, 0, 0, 0, 0, 0, 1, 3, 3, 3, 1, 4, 4, 4, 4, 2, 1, 3, 0
        };
        return Set[Jungseong];
    } else {
        static int Set[] = {
            5, 5, 5, 5, 5, 5, 5, 5, 6, 7, 7, 7, 6, 6, 7, 7, 7, 6, 6, 7, 5
        };
        return Set[Jungseong];
    }
}

int GetJungseongSet(int Choseong, int Jongseong)
{
    return (bool) Jongseong * 2 + (Choseong && Choseong != 15);
}

int GetJongseongSet(int Jungseong)
{
    static int Set[] = {
        0, 2, 0, 2, 1, 2, 1, 2, 3, 0, 2, 1, 3, 3, 1, 2, 1, 3, 3, 1, 1
    };
    return Set[Jungseong];
}

extern byte Buffer[32];

void ConstructHangul(wchar_t c)
{
    c -= 0xAC00;
    int Choseong  = c / 28 / 21;
    int Jungseong = c / 28 % 21;
    int Jongseong = c % 28;

    int Set;
    int Offset;

    Set = GetChoseongSet(Jungseong, Jongseong);
    Offset = (Set * 20 + Choseong + 1) * 32;
    for (int i = 0; i < 32; i++) {
        Buffer[i] = pgm_read_byte(&HangulFont[Offset + i]);
    }

    Set = GetJungseongSet(Choseong, Jongseong);
    Offset = (Set * 22 + Jungseong + 1) * 32 + 5120;
    for (int i = 0; i < 32; i++) {
        Buffer[i] |= pgm_read_byte(&HangulFont[Offset + i]);
    }

    Set = GetJongseongSet(Jungseong);
    Offset = (Set * 28 + Jongseong) * 32 + 7936;
    for (int i = 0; i < 32; i++) {
        Buffer[i] |= pgm_read_byte(&HangulFont[Offset + i]);
    }
}

int GetHangulPixel(int x, int y)
{
    int p;
    if (x < 8) {
        int m = 1 << (7 - x);
        p = Buffer[y * 2] & m;
    } else {
        int m = 1 << (15 - x);
        p = Buffer[y * 2 + 1] & m;
    }
    return p ? HIGH : LOW;
}
