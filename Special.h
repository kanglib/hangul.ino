extern byte Buffer[32];

int ConstructSpecial(wchar_t c)
{
    int i;
    for (i = 0; i < 288; i++) {
        if (pgm_read_word(&Ucs2ToKs[i]) == c) {
            break;
        }
    }
    if (i < 288) {
        for (int j = 0; j < 32; j++) {
            Buffer[j] = pgm_read_byte(&SpecialFont[i * 32 + j]);
        }
        return 1;
    } else {
        return 0;
    }
}

int GetSpecialPixel(int x, int y)
{
    int p;
    if (x < 8) {
        int m = 1 << (7 - x);
        p = Buffer[y * 2 + 1] & m;
    } else {
        int m = 1 << (15 - x);
        p = Buffer[y * 2] & m;
    }
    return p ? HIGH : LOW;
}
